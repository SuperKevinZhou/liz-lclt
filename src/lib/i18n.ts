type Messages = typeof zhCN;
export type TranslationKey = keyof Messages;

export function t(
  key: TranslationKey,
  replacements: Record<string, string | number> = {},
): string {
  let value = zhCN[key];
  for (const [name, replacement] of Object.entries(replacements)) {
    value = value.replace(
      `{${name}}`,
      String(replacement),
    ) as Messages[typeof key];
  }
  return value;
}
